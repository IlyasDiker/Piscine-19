#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	int tmp;

	tmp = 0;
	while (str[tmp])
	{
		ft_putchar(str[tmp]);
		tmp++;
	}
}
