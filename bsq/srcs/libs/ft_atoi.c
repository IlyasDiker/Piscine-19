#include "bsq.h"

int	ft_atoi(char *str)
{
	int		i;
	int		nb;
	int		n;

	i = 0;
	nb = 0;
	n = 0;
	while (str[i] == 32 || str[i] == 10 || str[i] == 9)
		i++;
	if (str[i] == 45)
		n = 1;
	if (str[i] == 45 || str[i] == 43)
		i++;
	while (str[i] >= 48 && str[i] <= 57)
	{
		nb *= 10;
		nb += str[i] - 48;
		i++;
	}
	return (n ? -nb : nb);
}
