int		ft_count_if(char **tab, int length, int (*f)(char*))
{
	int a;
	int nb;

	a = 0;
	nb = 0;
	while (tab[a][0] < length)
	{
		if (f(tab[a]) == 1)
			nb++;
		a++;
	}
	return (nb);
}
