#include "ft.h"

int		ft_add(int i, int j)
{
	return (i + j);
}

int		ft_division(int i, int j)
{
	return (i / j);
}

int		ft_minus(int i, int j)
{
	return (i - j);
}

int		ft_multiplication(int i, int j)
{
	return (i * j);
}

int		ft_modulo(int i, int j)
{
	return (i % j);
}
